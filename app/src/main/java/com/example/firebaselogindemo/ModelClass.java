package com.example.firebaselogindemo;

class ModelClass {
    public String name;
    public String age;

    public ModelClass() {
    }

    public ModelClass(String name, String age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
